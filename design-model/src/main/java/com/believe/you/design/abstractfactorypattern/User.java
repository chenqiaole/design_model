package com.believe.you.design.abstractfactorypattern;

public class User {
	private int mID;
	private String mName;

	public int getID() {
		return mID;
	}

	public void setID(int mID) {
		this.mID = mID;
	}

	public String getName() {
		return mName;
	}

	public void setName(String mName) {
		this.mName = mName;
	}

}